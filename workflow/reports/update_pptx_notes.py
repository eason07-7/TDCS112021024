def main():
    p = Presentation(str(PPTX))
    print(f"opened {PPTX.name}, {len(p.slides)} slides")
    for i, slide in enumerate(p.slides, 1):
        if i not in NEW_NOTES:
            continue
        if not slide.has_notes_slide:
            slide.notes_slide  # touch to create
        tf = slide.notes_slide.notes_text_frame
        tf.clear()
        # split into paragraphs by \n
        lines = NEW_NOTES[i].split("\n")
        # first paragraph already exists after clear()
        first = lines[0]
        tf.paragraphs[0].text = first
        for line in lines[1:]:
            para = tf.add_paragraph()
            para.text = line
        print(f"  updated p{i}")
    p.save(str(PPTX_OUT))
    print(f"saved -> {PPTX_OUT}")


if __name__ == "__main__":
    main()
